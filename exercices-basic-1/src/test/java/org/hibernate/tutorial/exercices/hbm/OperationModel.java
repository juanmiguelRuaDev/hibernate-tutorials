package org.hibernate.tutorial.exercices.hbm;

import java.util.Date;

public class OperationModel {
	
	private int id;
	private Date date;
	
	
	
	
	
	public OperationModel(){
		
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	
	

}
